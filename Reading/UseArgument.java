public class UseArgument {
    public static void main(String[] args) {
        System.out.print("Hi, ");
        System.out.print(args[0]);
        // prints out the first command line argument after program name
        // 0 means the first argument and so on
        System.out.println(". How are you?");
        System.out.println("I hope this is a new line.");
    }
}
